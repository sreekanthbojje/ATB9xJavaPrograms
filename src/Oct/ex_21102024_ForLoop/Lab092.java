package Oct.ex_21102024_ForLoop;

public class Lab092 {
    public static void main(String[] args) {
        for(float f = 0.0f; f <= 10.67; f++ ){
            System.out.println("Hi, float is ->  " + f );
        }
        for (long l = 1; l <= 10; l++){
            System.out.println(l);
        }
        for(char c = 'A'; c <= 'Z'; c++){
            System.out.println(c);
        }
    }
}
