package Oct.ex_18102024_IfCondition_Switch;

public class Lab083 {
    public static void main(String[] args) {
        char code = 'C' ;
        switch(code) {
            default :
                System.out.println("Hello");
                break;
            case 'A' :
                System.out.println("64");
                break;
            case 'B':
                System.out.println("65");
                break;
        }
    }
}
