package Oct.ex_11102024_Operators;

public class Lab040 {
    public static void main(String[] args) {
        // RELATIONAL OPERATORS are used to Compare
//        < (Less than)
//        <= (Less than or Equal to)
//        > (Greater than)
//        >= (Greater than or Equal to)
//        == (Equal to)
//        != (Not Equal to)
//        Return True or False

        int a = 10;
        int b = 30;
        boolean c = a > b;
        System.out.println(c);

        int age_mamitha = 34;
        int age_pramod = 34;
        boolean result = age_pramod >= age_mamitha;
        System.out.println(result);
    }
}
