package Oct.ex_09102024;

public class Lab025 {
    public static void main(String[] args) {
        int age = 10;
        System.out.printf("My age is %d", age);

        String name = "pramod";
        System.out.printf("Your name is %s",name);

        boolean b = true;
        System.out.printf("It is %b", b);

        // printf >>> It will replace the value in the %
//        %d >>> int, byte, short, long;
//        %s >>> string;
//        %f >>> float, double;
//        %b >>> boolean;
//        %x >>> Hex;
    }
}
